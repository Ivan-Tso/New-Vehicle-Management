def t(key, lang='zh'):
    """Translate a key to the given language. Falls back to key itself."""
    entry = TRANSLATIONS.get(key)
    if entry:
        return entry.get(lang, entry.get('zh', key))
    return key


def t_type(prefix, value, lang='zh'):
    """Translate a type value. e.g. t_type('mtype', 'routine', 'en') -> 'Routine Service'"""
    key = f'{prefix}.{value}'
    return t(key, lang)
